from __future__ import annotations

DIFF_SUMMARY_PROMPT_VERSION = "diff-summary-v2"


def build_diff_summary_prompt(diff_text: str, char_limit: int) -> str:
    return (
        "Ты пишешь описание коммита для разработчика, который смотрит историю проекта.\n"
        "Сфокусируйся не на построчном diff, а на сути изменения: какая часть системы затронута, "
        "зачем это могло быть сделано и как меняется поведение или сопровождение кода.\n"
        "Ответь на русском живым инженерным языком, без markdown, нумерации и списков.\n"
        "Первый абзац: 3-5 содержательных предложений с главным смыслом изменения.\n"
        "Если по diff видны реальные риски, спорные места или важные проверки, добавь второй короткий "
        "абзац и начни его с 'Риски и проверка:'. Не добавляй этот абзац ради формы.\n"
        "Упоминай конкретные файлы или модули только когда это помогает понять смысл; "
        "не перечисляй их механически.\n"
        "Не выдумывай контекст вне diff. Если данных мало, честно опиши только видимое изменение.\n\n"
        f"DIFF:\n{diff_text[:char_limit]}"
    )
